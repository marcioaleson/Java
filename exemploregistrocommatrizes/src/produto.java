public class produto{
	private int num;
	private String nome;
	private float preço;
	
	public void setnum(int n){
		num=n;
	}
	public int getnum(){
		return num;
	}
	public void setnome(String n){
		nome=n;
	}
	public String getnome(){
		return nome;
	}
	public void setpreço(float n){
	    preço=n;	
	}
	public float getpreço(){
		return preço;
	}
}